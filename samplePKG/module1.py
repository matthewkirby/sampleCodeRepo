def myAdd(a, b):
    """
    Adds two numbers together

    Parameters
    ----------
    a : int or float
        First of two numbers to add together
    b : int or float
        Second of two numbers to add together

    Returns
    -------
    int or float
        Returns the sum of the two parameters
    """
    return a+b

def mySub(a, b):
    return a-b

def myMul(a, b):
    return float(a*b)

def myDiv(a, b):
    return float(a)/float(b)
